public class Main {
    public static void main(String[] args) {
        //TIPOS DE DATOS PRIMITIVOS
        System.out.println("-----------------------------------");
        System.out.println("TIPOS DE DATOS PRIMITIVOS");
        System.out.println("-----------------------------------");

        //NÚMEROS ENTEROS
        byte entero = 127;
        System.out.println("-----------------------------------");
        System.out.println("NÚMEROS ENTEROS");
        System.out.println("-----------------------------------");
        System.out.println("Número entero (byte): " + entero);

        short entero2 = 12456;
        System.out.println("Número entero (short): " + entero2);

        int entero3 = 1245656;
        System.out.println("Número entero (int): " + entero3);

        long entero4 = 1245656565;
        System.out.println("Número entero (long): " + entero4);
        System.out.println("-----------------------------------");

        //NÚMEROS DECIMALES
        float decimal = 3.45f;
        System.out.println("NÚMEROS DECIMALES");
        System.out.println("-----------------------------------");
        System.out.println("Número decimal (float): " + decimal);

        double decimal2 = 3.4523421;
        System.out.println("Número decimal (double): " + decimal2);
        System.out.println("-----------------------------------");

        //NÚMEROS DE DATOS CARÁCTER
        char caracter = 'a';
        System.out.println("CARÁCTER");
        System.out.println("-----------------------------------");
        System.out.println("Número caracter: " + caracter);
        System.out.println("-----------------------------------");

        //NÚMEROS BOOLEANOS
        boolean decision = true;
        System.out.println("BOOLEANOS");
        System.out.println("-----------------------------------");
        System.out.println("La decisión es: " + decision);
        System.out.println("-----------------------------------");
    }
}