public class operadoresAritmeticos_Asignacion {

    public static void main(String[] args) {

        //OPERADORES ARITMETICOS DE ASIGNACIÃ“N
        int numero = 10;

        numero += 5; //numero = numero + 5
        System.out.println(numero);

        numero -= 5;
        System.out.println(numero);

        numero *= 5;
        System.out.println(numero);

        numero /= 5;
        System.out.println(numero);

        numero %= 5;
        System.out.println(numero);



    }

}
