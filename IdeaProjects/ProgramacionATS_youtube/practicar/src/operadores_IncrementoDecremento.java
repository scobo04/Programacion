public class operadores_IncrementoDecremento {

    public static void main(String[] args) {

        //OPERADORES DE INCREMENTO Y DECREMENTO
        int x = 5, y;

        //x++ postincrementa el valor de x
        y = x++;
        System.out.println(y);

        //++x preincrementa el valor de x
        y = ++x;
        System.out.println(y);

        //x-- postdecrementa el valor de x
        y = x--;
        System.out.println(y);

        //--x predecrementa el valor de x
        y = --x;
        System.out.println(y);


    }

}
