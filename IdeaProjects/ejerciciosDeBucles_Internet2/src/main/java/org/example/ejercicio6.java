public class ejercicio6 {

    public static void main(String[] args) {

        int n1 = 11;

        esDivisibleDos(n1);

    }

    public static void esDivisibleDos(int n1) {

        if (n1 % 2 == 0) {

            System.out.println("Es divisible por dos");

        } else {

            System.out.println("No es divisible por dos");

        }

    }

}
